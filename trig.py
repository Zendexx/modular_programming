import math

# finding sin of a number

def get_sin(x):
    angle_rad=math.radians(x)
    angle_sin=math.sin(angle_rad)
    return angle_sin
def get_tan(x):
    angle_rad=math.radians(x)
    angle_tan=math.tan(angle_rad)
    return angle_rad
def get_cosin(x):
    angle_rad=math.radians(x)
    x=math.cos(angle_rad)
    return x
